// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
    var arrayTotal = 0;
    nums = array.length;
    for ( var i = 0; i < nums; i ++ ) {
      arrayTotal += array[i];
    }
    return arrayTotal;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  let reverseArray = [];
  for (let i = array.length - 1; i >= 0; i--) {
    reverseArray.push((array[i]));
  }
  return reverseArray;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array) {
  var i = 0;
  while (i < array.length - 1) {
    array.splice(i, 0, array.pop());
    i++;
  }
  return array;
}

