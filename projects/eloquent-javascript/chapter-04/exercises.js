function range(start, end) {
  let rangeArray = [];
    if (start < end) {
		for (var i = start; i <= end; i++) {
			rangeArray.push(i);
		}
	} else if (start > end) {
		for (var j = start; j >= end; j--) {
            rangeArray.push(j);
            }
	}
    return rangeArray;
}

