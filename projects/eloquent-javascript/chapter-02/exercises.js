function triangles(input) {
  for (let triangle = "#"; triangle.length <= input; triangle += "#") {
    console.log(triangle);
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (var i = 1; i <= 15; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
        console.log("fizzbuzz");
    } else if (i % 3 === 0) {
        console.log("fizz");
    } else if (i % 5 === 0) {
        console.log("buzz");
    } else {
        console.log(i);
    }
}
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
  let size = num;
  let char = "";

    for (var j = 0; j < size; j++) {
	    for (var i = 0; i < size; i++) {
      	  if ((i + j) % 2 === 0) {
        	  char += " ";
          } else {
        	  char += "#";
        }
      }
  char += "\n";
}

console.log(char);
}

