////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
	if (num1 > num2) {
		return num2;
	} else {
		return num1;
	}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(wholeNum) {
	if (wholeNum % 2 === 0) {
		return true;
	} else if (wholeNum % 2 !== 0) {
		return false;
	} else {
		return false;
	}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let counter = [];
	for (let i = 0; i < string.length; i++) {
		if (string[i] === char) {
			counter.push(char);
		}
	}
	return counter.length;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let counter = [];
	for (let i = 0; i < string.length; i++) {
		if (string[i] === "B") {
			counter.push(string[i]);
		} 
	}
	return counter.length;
}

